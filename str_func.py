def change_str(input_str):
    '''
    функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами
    '''
    return input_str.upper()

def cap_str(str_input):
    '''
    функция,
    которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции.
    '''
    return str_input.title()